-- Write a solution to find the IDS of the users who visited without making any transactions and the no of times they made these types of visits

/*


Input: 
Table1: Visits
+----------+-------------+
| visit_id | customer_id |
+----------+-------------+
| 1        | 23          |
| 2        | 9           |
| 4        | 30          |
| 5        | 54          |
| 6        | 96          |
| 7        | 54          |
| 8        | 54          |
+----------+-------------+

Table2: Transactions
+----------------+----------+--------+
| transaction_id | visit_id | amount |
+----------------+----------+--------+
| 2              | 5        | 310    |
| 3              | 5        | 300    |
| 9              | 5        | 200    |
| 12             | 1        | 910    |
| 13             | 2        | 970    |
+----------------+----------+--------+
Output: 
+-------------+----------------+
| customer_id | count_no_trans |
+-------------+----------------+
| 54          | 2              |
| 30          | 1              |
| 96          | 1              |
+-------------+----------------+
*/

SELECT visit.customer_id, COUNT(customer_id) AS count_no_trans
FROM Visits visit
LEFT JOIN Transactions txns
ON visit.visit_id = txns.viewer_id
WHERE txns.transaction_id is NULL
GROUP BY visit.customer_id

